package programs_on_numbers;

public class NaturalNumbers {

	public static void main(String[] args) {

		System.out.print("Natural numbers are : ");

		int a=20;

		for (int b=1; b<=a; b++) {

			System.out.print(" "+b);
		}
	}
}

